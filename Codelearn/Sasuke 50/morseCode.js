function morseCode(str){
    const map = {
        A:[1,1], B:[3,1], C:[2,2], D:[2,1],
        E:[1,0], F:[3,1], G:[1,2], H:[4,0],
        I:[2,0], J:[1,3], K:[1,2], L:[3,1],
        M:[0,2], N:[1,1], O:[0,3], P:[2,2],
        Q:[1,3], R:[2,1], S:[3,0], T:[0,1],
        U:[2,1], V:[3,1], W:[1,2], X:[2,2],
        Y:[1,3], Z:[2,2], 1:[1,4], 2:[2,3],
        3:[3,2], 4:[4,1], 5:[5,0], 6:[4,1],
        7:[3,2], 8:[2,3], 9:[1,4], 0:[0,5]
    };
 
    let x = 6; let y = 5;
    for(let i = 0; i < str.length; i++) {
        if(str[i] !== ' ') {
            // console.log(str[i], map[str[i]]);
            const a = map[str[i]];
            x += a[0];
            y += a[1];
        }
    }
 
    return [x,y];
}